package day1.examples;

public class Examplefor {

	public static void main(String[] args) {
		System.out.println("incrementing from 0 to 10");
		for(int i=0;i<=10;i++)
			System.out.println("i="+i);
		System.out.println("decrementing from 10 to 0");
		for(int i=10;i>=0;i--)
			System.out.println("i="+i);

	}

}
